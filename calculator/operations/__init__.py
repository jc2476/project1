"""These are operation classes"""

@staticmethod
class Addition:
    """Addition class"""

    def add(value_1, value_2):
        """Add method"""
        return value_1 + value_2


@staticmethod
class Subtraction:
    """Subtraction class"""

    def subtract(value_1, value_2):
        """Subtract method"""
        return value_1 - value_2


@staticmethod
class Multiplication:
    """Multiplication class"""

    def multiply(value_1, value_2):
        """Multiply method"""
        return value_1 * value_2


@staticmethod
class Division:
    """Division class"""

    def divide(value_1, value_2):
        """Divide method"""
        return value_1 / value_2
